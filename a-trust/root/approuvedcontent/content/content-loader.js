document.addEventListener('DOMContentLoaded', () => {
  // Récupérer l'ID du contenu depuis l'URL
  const urlParams = new URLSearchParams(window.location.search);
  const contentId = urlParams.get('id');

  if (contentId && contentData[contentId]) {
    loadContent(contentId);
    updateMetaTags(contentData[contentId]);
  } else {
    showError();
  }
});

function loadContent(contentId) {
  const content = contentData[contentId];
  const container = document.getElementById('content-container');

  container.innerHTML = `
    <div class="content-hero">
      <img src="${content.image}" alt="${content.title}" class="content-poster">
      <div class="content-details">
        <h1 class="section-title">${content.title}</h1>
        <div class="content-meta">
          ${Object.values(content.meta).map(tag => `<span class="meta-tag">${tag}</span>`).join('')}
        </div>
        <div class="rating-stars">${content.stars}</div>
        <p class="content-description">${content.description}</p>
        <div class="content-actions">
          <a href="${content.watchLink}" class="cta-button">${content.ctaButton}</a>
          <a href="/warning" class="bit-button">Alternative</a>
        </div>
      </div>
    </div>

    <div class="content-sections">
      <!-- Détails -->
      <section class="content-section">
        <h2>Détails</h2>
        <p>Créateur : ${content.details.creators}</p>
        <p>Distribution : ${content.details.cast}</p>
      </section>

      <!-- Avis -->
      <section class="content-section">
        <h2>Avis</h2>
        <div class="review-grid">
          ${content.reviews.map(review => `
            <div class="review-card">
              <div class="review-header">
                <div class="reviewer-avatar"></div>
                <div>
                  <h4>${review.author}</h4>
                  <div class="rating-stars">${review.rating}</div>
                </div>
              </div>
              <p>${review.text}</p>
            </div>
          `).join('')}
        </div>
      </section>

      <!-- Contenu similaire -->
      <section class="content-section">
        <h2>Contenu similaire</h2>
        <div class="related-content">
          ${content.relatedContent.map(related => `
            <div class="related-card">
              <img src="${related.image}" alt="${related.title}" class="related-image">
              <div class="related-info">
                <h3>${related.title}</h3>
              </div>
            </div>
          `).join('')}
        </div>
      </section>
    </div>
  `;
}

function updateMetaTags(content) {
  document.title = `VentiStudio - ${content.title}`;
  // Mettre à jour les meta tags
  document.querySelector('meta[property="og:title"]').content = `VentiStudio - ${content.title}`;
  document.querySelector('meta[property="og:description"]').content = content.description;
  document.querySelector('meta[property="og:image"]').content = content.image;
}

function showError() {
  document.getElementById('content-container').innerHTML = `
    <div class="error-message">
      <h2>Contenu non trouvé</h2>
      <p>Le contenu demandé n'existe pas ou n'est pas accessible.</p>
    </div>
  `;
}